				  $(".enclave").click(function() {
					  $('.middle1').animate({"right":"-100%",opacity:1},500);
					  $('#upcoming').animate({"right":"-100%",opacity:1},500);
					  $('#ongoing').animate({"right":"-100%",opacity:1},500);
					  $('.middle').animate({"right":"12%",opacity:1},500);
