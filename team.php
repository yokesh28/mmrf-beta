<h4>Our mission as a workplace<br />
feeds our mission as a business.</h4>
<p><strong>Our mission as a workplace</strong> is to be just as productive as the folks who work here everyday building software. Indeed, building an efficient company requires just as much design, craft, and focus as building a high-availability software system. Effective project management, sharp IT support, a healthy fridge, comprehensive benefits, and helping folks be proactive team members are just a few ways we express our view that the gateway to a successful business is an efficient workplace.</p>
<p><strong>Our mission as a business</strong> is to build web apps and services which help other companies be better places to work at too. Every company should work smarter, hire better, and organize quicker. Our applications and consulting services show them how.</p>
